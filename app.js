function renderQuizQuestion() {
  if (!quizModal || !shuffledQuestions || shuffledQuestions.length === 0) {
    console.warn('No quiz modal or questions');
    return;
  }
  
  if (currentQIndex < 0 || currentQIndex >= shuffledQuestions.length) {
    console.error('Invalid question index:', currentQIndex);
    return;
  }
  
  console.log(`лЪлЙл║л░лил▓л░лйлх лйл░ л▓Ліл┐ЛђлЙЛЂ ${currentQIndex + 1}`);
  
  const q = shuffledQuestions[currentQIndex];
  const body = quizModal.querySelector('.modal__body');
  const footer = quizModal.querySelector('.modal__footer');
  const progress = quizModal.querySelector('#quizProgress');
  const heading = quizModal.querySelector('#quizHeading');

  if (heading) heading.textContent = `­Ъј» лњЛіл┐ЛђлЙЛЂ ${currentQIndex + 1}`;
  if (progress) progress.textContent = `${currentQIndex + 1} лЙЛѓ ${shuffledQuestions.length}`;

  if (body) {
    const inputType = Array.isArray(q.correctIndex) ? 'checkbox' : 'radio';
    const inputName = Array.isArray(q.correctIndex) ? 'quizAnswers' : 'quizAnswer';
    
    body.innerHTML = `
      <div class="quiz-question">${q.q}</div>
      <ul class="quiz-options">
        ${q.answers
          .map(
            (choice, i) => `
            <li>
              <label>
                <input type="${inputType}" name="${inputName}" value="${i}" ${
                  Array.isArray(q.correctIndex) 
                    ? (selectedAnswers[currentQIndex] && selectedAnswers[currentQIndex].includes(i) ? 'checked' : '')
                    : (selectedAnswers[currentQIndex] === i ? 'checked' : '')
                }>
                <span>${choice}</span>
              </label>
            </li>`
          )
          .join('')}
      </ul>`;

    // лЌл░л┐л░лил▓л░лйлх лйл░ лЙЛѓл│лЙл▓лЙЛђл░
    const inputs = body.querySelectorAll(`input[name="${inputName}"]`);
    inputs.forEach((input) => {
      input.addEventListener('change', (e) => {
        if (Array.isArray(q.correctIndex)) {
          // Multi-choice
          if (!selectedAnswers[currentQIndex]) selectedAnswers[currentQIndex] = [];
          if (e.target.checked) {
            if (!selectedAnswers[currentQIndex].includes(parseInt(e.target.value))) {
              selectedAnswers[currentQIndex].push(parseInt(e.target.value));
            }
          } else {
            selectedAnswers[currentQIndex] = selectedAnswers[currentQIndex].filter(x => x !== parseInt(e.target.value));
          }
        } else {
          // Single choice
          selectedAnswers[currentQIndex] = parseInt(e.target.value);
        }
        
        // лњлИлиЛЃл░л╗лхлй feedback
        updateQuestionStyles(body, inputName);
      });
    });
    
    // Initial styles
    updateQuestionStyles(body, inputName);
  }

  if (footer) {
    footer.innerHTML = `
      ${currentQIndex > 0 ? '<button class="btn btn--secondary" id="prevQuestion">РгЁ лЮл░лил░л┤</button>' : '<span></span>'}
      <button class="btn btn--primary" id="nextQuestion">${currentQIndex === shuffledQuestions.length - 1 ? '­ЪЈЂ лЌл░л▓ЛіЛђЛѕлИ' : 'лЮл░л┐Лђлхл┤ РъА'}</button>
    `;

    const prevBtn = footer.querySelector('#prevQuestion');
    const nextBtn = footer.querySelector('#nextQuestion');
    
    if (prevBtn) {
      prevBtn.onclick = (e) => {
        e.preventDefault();
        currentQIndex--;
        renderQuizQuestion();
      };
    }
    
    if (nextBtn) {
      nextBtn.onclick = (e) => {
        e.preventDefault();
        if (currentQIndex === shuffledQuestions.length - 1) {
          finishQuiz();
        } else {
          currentQIndex++;
          renderQuizQuestion();
        }
      };
    }
  }
}

function updateQuestionStyles(body, inputName) {
  const labels = body.querySelectorAll('label');
  labels.forEach(label => {
    const input = label.querySelector('input');
    if (input && input.checked) {
      label.style.borderColor = 'var(--color-primary)';
      label.style.background = 'var(--gradient-subtle)';
    } else {
      label.style.borderColor = 'var(--color-border-light)';
      label.style.background = 'transparent';
    }
  });
}

function finishQuiz() {
  if (!shuffledQuestions || !selectedAnswers) {
    console.error('Quiz data is invalid');
    showErrorMessage('лЊЛђлхЛѕл║л░ л┐ЛђлИ лЙл▒Лђл░л▒лЙЛѓл║л░ лйл░ ЛђлхлиЛЃл╗Лѓл░ЛѓлИЛѓлх');
    return;
  }
  
  console.log('лЌл░л▓ЛіЛђЛѕл▓л░лйлх лйл░ ЛѓлхЛЂЛѓл░...');
  let correct = 0;
  
  shuffledQuestions.forEach((q, i) => {
    const userAnswer = selectedAnswers[i];
    
    if (Array.isArray(q.correctIndex)) {
      // Multi-choice л▓Ліл┐ЛђлЙЛЂ
      const userAnswers = userAnswer || [];
      const correctAnswers = q.correctIndex;
      
      if (arraysEqual(userAnswers.sort(), correctAnswers.sort())) {
        correct++;
      }
    } else {
      // Single choice л▓Ліл┐ЛђлЙЛЂ
      if (userAnswer === q.correctIndex) {
        correct++;
      }
    }
  });
  
  const percent = Math.round((correct / shuffledQuestions.length) * 100);
  
  // лЉлхлилЙл┐л░ЛЂлйлЙ лил░л┐лИЛЂл▓л░лйлх лйл░ ЛђлхлиЛЃл╗Лѓл░Лѓл░
  if (currentTopic && currentTopic.id && progressMap[currentTopic.id]) {
    progressMap[currentTopic.id].quizScore = percent;
  }
  
  console.log(`лалхлиЛЃл╗Лѓл░Лѓ: ${correct}/${shuffledQuestions.length} (${percent}%)`);
  
  refreshSidebarStatus();
  updateProgressBar();
  updateTopicBadge();
  
  showQuizResults(correct, percent);
}

// лЪлЙл╝лЙЛЅлйл░ ЛёЛЃлйл║ЛєлИЛЈ лил░ ЛЂЛђл░л▓лйЛЈл▓л░лйлх лйл░ л╝л░ЛЂлИл▓лИ
function arraysEqual(a, b) {
  return Array.isArray(a) && Array.isArray(b) && 
         a.length === b.length && 
         a.every((val, i) => val === b[i]);
}

function showQuizResults(score, percent) {
  const body = quizModal.querySelector('.modal__body');
  const footer = quizModal.querySelector('.modal__footer');
  const heading = quizModal.querySelector('#quizHeading');
  const progress = quizModal.querySelector('#quizProgress');

  if (heading) heading.textContent = '­ЪјЅ лалхлиЛЃл╗Лѓл░ЛѓлИ лЙЛѓ ЛѓлхЛЂЛѓл░';
  if (progress) progress.textContent = '';

  const emoji = percent >= 80 ? '­ЪЈє' : percent >= 60 ? '­Ъўі' : percent >= 40 ? '­Ъўљ' : '­Ъўъ';
  const message = percent >= 80 ? 'лъЛѓл╗лИЛЄлхлй ЛђлхлиЛЃл╗Лѓл░Лѓ!' : percent >= 60 ? 'лћлЙл▒ЛіЛђ ЛђлхлиЛЃл╗Лѓл░Лѓ!' : 'люлЙлХлхЛѓлх л┤л░ ЛЂлх л┐лЙл┤лЙл▒ЛђлИЛѓлх!';

  let html = `
    <div class="result-summary">
      <div style="font-size: 3rem; margin-bottom: 1rem;">${emoji}</div>
      <h3>${message}</h3>
      <p style="font-size: 1.2rem;">лњлхЛђлйлИ лЙЛѓл│лЙл▓лЙЛђлИ: <strong>${score}</strong> лЙЛѓ ${shuffledQuestions.length} (<strong>${percent}%</strong>)</p>
    </div>
    
    <h4>лЪлЙл┤ЛђлЙл▒лхлй л┐Лђлхл│л╗лхл┤:</h4>
  `;

  shuffledQuestions.forEach((q, i) => {
    const userAnswer = selectedAnswers[i];
    let isCorrect = false;
    
    if (Array.isArray(q.correctIndex)) {
      const userAnswers = userAnswer || [];
      const correctAnswers = q.correctIndex;
      isCorrect = arraysEqual(userAnswers.sort(), correctAnswers.sort());
    } else {
      isCorrect = userAnswer === q.correctIndex;
    }
    
    let userAnswerText = 'лЉлхли лЙЛѓл│лЙл▓лЙЛђ';
    if (Array.isArray(q.correctIndex)) {
      if (userAnswer && userAnswer.length > 0) {
        userAnswerText = userAnswer.map(idx => q.answers[idx]).join(', ');
      }
    } else {
      if (userAnswer !== null && userAnswer !== undefined) {
        userAnswerText = q.answers[userAnswer];
      }
    }
    
    let correctAnswerText = '';
    if (Array.isArray(q.correctIndex)) {
      correctAnswerText = q.correctIndex.map(idx => q.answers[idx]).join(', ');
    } else {
      correctAnswerText = q.answers[q.correctIndex];
    }
    
    html += `
      <div class="question-result ${isCorrect ? 'correct' : 'incorrect'}">
        <p><strong>лњЛіл┐ЛђлЙЛЂ ${i + 1}:</strong> ${q.q}</p>
        <p><strong>лњл░ЛѕлИЛЈЛѓ лЙЛѓл│лЙл▓лЙЛђ:</strong> ${userAnswerText} ${isCorrect ? 'РюЊ' : 'РюЌ'}</p>
        ${!isCorrect ? `<p><strong>лЪЛђл░л▓лИл╗лйлИЛЈЛѓ лЙЛѓл│лЙл▓лЙЛђ:</strong> ${correctAnswerText}</p>` : ''}
        <p><em>­ЪњА лъл▒ЛЈЛЂлйлхлйлИлх:</em> ${q.explanation}</p>
      </div>
    `;
  });

  if (body) body.innerHTML = html;

  if (footer) {
    footer.innerHTML = `
      <button class="btn btn--secondary" id="retryQuiz">­Ъћё лъл┐лИЛѓл░л╣ лЙЛѓлйлЙл▓лЙ</button>
      <button class="btn btn--primary" id="closeQuiz">РюЊ лЌл░Лѓл▓лЙЛђлИ</button>
    `;
    
    const retryBtn = footer.querySelector('#retryQuiz');
    const closeBtn = footer.querySelector('#closeQuiz');
    
    if (retryBtn) {
      retryBtn.onclick = (e) => {
        e.preventDefault();
        startQuiz();
      };
    }
    if (closeBtn) {
      closeBtn.onclick = (e) => {
        e.preventDefault();
        closeQuizModal();
      };
    }
  }
}

